timing_test(function() {
  at(0 * 1000, function() {
    assert_styles(
      '.anim',
      [{'left':'100px'},
       {'left':'100px'},
       {'left':'0px'},
       {'left':'0px'}]);
  }, "Autogenerated");
  at(0.5 * 1000, function() {
    assert_styles(
      '.anim',
      [{'left':'150px'},
       {'left':'125px'},
       {'left':'0px'},
       {'left':'0px'}]);
  }, "Autogenerated");
  at(1.0 * 1000, function() {
    assert_styles(
      '.anim',
      [{'left':'200px'},
       {'left':'150px'},
       {'left':'0px'},
       {'left':'0px'}]);
  }, "Autogenerated");
  at(1.5 * 1000, function() {
    assert_styles(
      '.anim',
      [{'left':'200px'},
       {'left':'175px'},
       {'left':'0px'},
       {'left':'0px'}]);
  }, "Autogenerated");
  at(2.0 * 1000, function() {
    assert_styles(
      '.anim',
      [{'left':'200px'},
       {'left':'200px'},
       {'left':'100px'},
       {'left':'100px'}]);
  }, "Autogenerated");
  at(2.5 * 1000, function() {
    assert_styles(
      '.anim',
      [{'left':'200px'},
       {'left':'200px'},
       {'left':'150px'},
       {'left':'125px'}]);
  }, "Autogenerated");
  at(3.0 * 1000, function() {
    assert_styles(
      '.anim',
      [{'left':'200px'},
       {'left':'200px'},
       {'left':'200px'},
       {'left':'150px'}]);
  }, "Autogenerated");
  at(3.5 * 1000, function() {
    assert_styles(
      '.anim',
      [{'left':'200px'},
       {'left':'200px'},
       {'left':'200px'},
       {'left':'175px'}]);
  }, "Autogenerated");
  at(4.0 * 1000, function() {
    assert_styles('.anim', {'left':'200px'});
  }, "Autogenerated");
}, "Autogenerated checks.");
