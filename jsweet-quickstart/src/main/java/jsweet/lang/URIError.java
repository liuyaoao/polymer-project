package jsweet.lang;
/** <p>The <code><strong>URIError</strong></code> object represents an error when a global URI handling function was used in a wrong way.</p>  */
@java.lang.SuppressWarnings("serial")
public class URIError extends Error {
    public URIError(java.lang.String message){}
    native public static URIError applyStatic(java.lang.String message);
    /** 
 Allows the addition of properties to a <code>URIError</code> object.  */
    public static final URIError prototype=null;
    public URIError(){}
    native public static URIError applyStatic();
}

