package jsweet.dom;
/** <p>The <code>SVGRadialGradientElement</code> interface corresponds to the <code>&lt;radialGradient&gt;</code> element.</p>  */
public class SVGRadialGradientElement extends SVGGradientElement {
    /** 
Corresponds to attribute <code>cx</code> on the given <code>&lt;radialGradient&gt;</code> element. */
    public SVGAnimatedLength cx;
    /** 
Corresponds to attribute <code>cy</code> on the given <code>&lt;radialGradient&gt;</code> element. */
    public SVGAnimatedLength cy;
    /** 
Corresponds to attribute <code>fx</code> on the given <code>&lt;radialGradient&gt;</code> element. */
    public SVGAnimatedLength fx;
    /** 
Corresponds to attribute <code>fy</code> on the given <code>&lt;radialGradient&gt;</code> element. */
    public SVGAnimatedLength fy;
    /** 
Corresponds to attribute <code>r</code> on the given <code>&lt;radialGradient&gt;</code> element. */
    public SVGAnimatedLength r;
    public static SVGRadialGradientElement prototype;
    public SVGRadialGradientElement(){}
}

