package jsweet.dom;
/** <p>The <strong><code>HTMLLegendElement</code></strong> is an interface allowing to access properties of the <code>&lt;legend&gt;</code> elements. It inherits properties and methods from the <code>HTMLElement</code> interface.</p>  */
public class HTMLLegendElement extends HTMLElement {
    /**
      * Retrieves a reference to the form that the object is embedded in.
      */
    public String align;
    /**
      * Retrieves a reference to the form that the object is embedded in.
      */
    public HTMLFormElement form;
    public static HTMLLegendElement prototype;
    public HTMLLegendElement(){}
}

