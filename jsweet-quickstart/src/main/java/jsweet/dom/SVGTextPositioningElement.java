package jsweet.dom;
/** <p>The <code>SVGTextPositioningElement</code> interface is inherited by text-related interfaces: <code>SVGTextElement</code>, <code>SVGTSpanElement</code>, <code>SVGTRefElement</code> and <code>SVGAltGlyphElement</code>.</p>  */
public class SVGTextPositioningElement extends SVGTextContentElement {
    /** 
Corresponds to attribute <code>dx</code> on the given element. */
    public SVGAnimatedLengthList dx;
    /** 
Corresponds to attribute <code>dy</code> on the given element. */
    public SVGAnimatedLengthList dy;
    /** 
Corresponds to attribute <code>rotate</code> on the given element. */
    public SVGAnimatedNumberList rotate;
    /** 
Corresponds to attribute <code>x</code> on the given element. */
    public SVGAnimatedLengthList x;
    /** 
Corresponds to attribute <code>y</code> on the given element. */
    public SVGAnimatedLengthList y;
    public static SVGTextPositioningElement prototype;
    public SVGTextPositioningElement(){}
}

