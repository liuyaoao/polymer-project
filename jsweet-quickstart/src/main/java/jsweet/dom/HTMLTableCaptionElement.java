package jsweet.dom;
/** <p>The <strong><code>HTMLTableCaptionElement</code></strong> interface special properties (beyond the regular <code>HTMLElement</code> interface it also has available to it by inheritance) for manipulating table caption elements.</p>  */
public class HTMLTableCaptionElement extends HTMLElement {
    /**
      * Sets or retrieves the alignment of the caption or legend.
      */
    public String align;
    /**
      * Sets or retrieves whether the caption appears at the top or bottom of the table.
      */
    public String vAlign;
    public static HTMLTableCaptionElement prototype;
    public HTMLTableCaptionElement(){}
}

