package jsweet.dom;
/** <p>The <code>SVGLinearGradientElement</code> interface corresponds to the <code>&lt;linearGradient&gt;</code> element.</p>  */
public class SVGLinearGradientElement extends SVGGradientElement {
    /** 
Corresponds to attribute <code>x1</code> on the given <code>&lt;linearGradient&gt;</code> element. */
    public SVGAnimatedLength x1;
    /** 
Corresponds to attribute <code>x2</code> on the given <code>&lt;linearGradient&gt;</code> element. */
    public SVGAnimatedLength x2;
    /** 
Corresponds to attribute <code>y1</code> on the given <code>&lt;linearGradient&gt;</code> element. */
    public SVGAnimatedLength y1;
    /** 
Corresponds to attribute <code>y2</code> on the given <code>&lt;linearGradient&gt;</code> element. */
    public SVGAnimatedLength y2;
    public static SVGLinearGradientElement prototype;
    public SVGLinearGradientElement(){}
}

