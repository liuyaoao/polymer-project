package jsweet.dom;
/** <p>The <strong><code>HTMLParagraphElement</code></strong> interface provides special properties (beyond those of the regular <code>HTMLElement</code> object interface it inherits) for manipulating <code>&lt;p&gt;</code> elements.</p>  */
public class HTMLParagraphElement extends HTMLElement {
    /**
      * Sets or retrieves how the object is aligned with adjacent text. 
      */
    public String align;
    public String clear;
    public static HTMLParagraphElement prototype;
    public HTMLParagraphElement(){}
}

