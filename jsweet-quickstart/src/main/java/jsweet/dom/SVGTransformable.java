package jsweet.dom;
/** <p>Interface <code>SVGTransformable</code> contains properties and methods that apply to all elements which have attribute <code>transform</code>.</p>  */
@jsweet.lang.Interface
public abstract class SVGTransformable extends SVGLocatable {
    /** 
Corresponds to attribute <code>transform</code> on the given element. */
    public SVGAnimatedTransformList transform;
}

